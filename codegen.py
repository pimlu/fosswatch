def bitbang(st):
    og_st = st
    st = st.replace("\n", "")
    assert len(st) == 40, og_st
    buf = []
    for ch in reversed(st):
        buf += str(int(ch == "#"))
    return f"0b{"".join(buf)}ll,"


digits = list(map(str, range(10))) + [":", " "]
all = list(map(chr, range(256)))

files = ["src/digit_bmp.txt", "src/font.txt"]




for file, glyphs in zip(files, [digits, all]):
    glyph_strings = [FONT.get(glyph, " " * 40) for glyph in glyphs]

    with open(file, "w") as fh:

        fh.write("\n".join(map(bitbang, glyph_strings)))

    
